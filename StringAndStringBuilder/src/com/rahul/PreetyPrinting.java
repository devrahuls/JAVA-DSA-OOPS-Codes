package com.rahul;

public class PreetyPrinting {
    public static void main(String[] args) {
        float ff = 2.3552f;
        System.out.printf("Pie is: %.3f", ff); //%f is used to print the float format specifier, .3 is used to define that we want
        //only 3 digits after the decimal.

        System.out.printf("%n"); //New Line

        System.out.printf("Hello I am %s , and I am %s", "Rahul", "Cool"); //%s is used to print the String format specifier.

    }
}
